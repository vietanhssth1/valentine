    function openGift(person) {
        $('#giftModal h5').html(person + "'S GIFT BOX");
        $('#gift_box').val(person);
        var title = "";
        if (person == 'DORAEMON') {
            title = "CONGRATULATIONS 🎉 <br> 1 VÉ ĐI CÔNG VIÊN KING";
        } else if (person == 'NOBITA') {
            title = "CONGRATULATIONS 🎉 <br> 1 CẶP ÁO ĐÔI";
        } else if (person == 'SHIZUKA') {
            title = "CONGRATULATIONS 🎉 <br> 1 CHUYẾN ĐI PHƯỢT";
        } else if (person == 'SUNEO') {
            title = "CONGRATULATIONS 🎉 <br> 1 HỘP QUÀ HOA LÃNG MẠN";
        } else {
            alert('error');
            return ;
        }
        $('#giftModal .title').html(title);
        $('#giftModal').show();
    }
    function closeGift() {
        $('#giftModal').hide();
        $('#gift_box').val('');
    }
    function receiveGift() {
        $.ajax({
            type: 'POST',
            url: "/receive-gift",
            data: {
                "_token": "{{ csrf_token() }}",
                "gift_box": $('#gift_box').val()
            },
            dataType: 'json',
            success: function (response) {
                $('#giftModal').hide();
                $('#gift_box').val('');
            }
        });
    }
    function confirm() {
        var confirm = $('#confirm-input').find(":selected").val();
        if (!confirm || confirm == '') {
            alert("Ơ kìa chọn mục Xác nhận đi chứ!");
            return ;
        }
        var rate = $('#rate-input').find(":selected").val();
        if (!rate || rate == '') {
            alert("Ơ kìa chọn mục Đánh giá đi chứ!");
            return ;
        }
        var message = $('#message-input').val();
        if (!message || message == '') {
            alert("Ơ kìa phải để lại Lời nhắn chứ!");
            return ;
        }

        $.ajax({
            type: 'POST',
            url: "/confirm",
            data: {
                "_token": "{{ csrf_token() }}",
                "confirm": confirm,
                "rate": rate,
                "message": message
            },
            dataType: 'json',
            success: function (response) {
                $('#thank-you').html("Cảm ơn đã xác nhận, thông tin này sẽ được gửi vào email của Việt Anh.");
                $('#confirm-input').prop('selectedIndex',0);
                $('#rate-input').prop('selectedIndex',0);
                $('#message-input').val('');
                setTimeout(function () {
                    $('#thank-you').html('');
                }, 5000)
            }
        });
    }
